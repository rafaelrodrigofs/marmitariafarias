    <script>
        // Inicializar AOS (Animate On Scroll)
        document.addEventListener('DOMContentLoaded', function() {
            // Inicializar AOS
            AOS.init({
                duration: 800,
                easing: 'ease',
                once: true
            });
            
            // Menu mobile
            const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
            const navLinks = document.querySelector('.nav-links');
            
            if (mobileMenuBtn) {
                mobileMenuBtn.addEventListener('click', function() {
                    navLinks.classList.toggle('active');
                    this.classList.toggle('active');
                });
            }
            
            // Carrossel de depoimentos
            const testimonialsContainer = document.querySelector('.testimonials-container');
            const prevBtn = document.querySelector('.prev-btn');
            const nextBtn = document.querySelector('.next-btn');
            const testimonials = document.querySelectorAll('.testimonial');
            
            let currentIndex = 0;
            const testimonialWidth = 100; // 100%
            
            // Configurar o carrossel
            function updateCarousel() {
                testimonialsContainer.style.transform = `translateX(-${currentIndex * testimonialWidth}%)`;
            }
            
            // Botões de navegação
            if (prevBtn && nextBtn) {
                prevBtn.addEventListener('click', function() {
                    if (currentIndex > 0) {
                        currentIndex--;
                        updateCarousel();
                    } else {
                        // Voltar para o último slide
                        currentIndex = testimonials.length - 1;
                        updateCarousel();
                    }
                });
                
                nextBtn.addEventListener('click', function() {
                    if (currentIndex < testimonials.length - 1) {
                        currentIndex++;
                        updateCarousel();
                    } else {
                        // Voltar para o primeiro slide
                        currentIndex = 0;
                        updateCarousel();
                    }
                });
            }
            
            // Auto-play do carrossel (opcional)
            setInterval(function() {
                if (currentIndex < testimonials.length - 1) {
                    currentIndex++;
                } else {
                    currentIndex = 0;
                }
                updateCarousel();
            }, 8000); // Alterado de 5000 para 8000 - Muda a cada 8 segundos
            
            // Remover atributos data-aos temporariamente para garantir que o conteúdo seja exibido
            document.querySelectorAll('[data-aos]').forEach(element => {
                // Armazenar o valor original
                const originalAos = element.getAttribute('data-aos');
                element.setAttribute('data-aos-original', originalAos);
                element.removeAttribute('data-aos');
                
                // Restaurar após um pequeno atraso
                setTimeout(() => {
                    element.setAttribute('data-aos', element.getAttribute('data-aos-original'));
                    element.removeAttribute('data-aos-original');
                }, 100);
            });
        });
    </script>
